////////////////////////////////////////////////////////////////////////////////////////////////////

// standard libraries
use clap::ArgMatches;

////////////////////////////////////////////////////////////////////////////////////////////////////

pub struct BoolSettings {
  pub verbose: bool,
  pub dry_run: bool,
  pub force:   bool,
}

pub fn bool_collector(matches: &ArgMatches) -> BoolSettings {
  BoolSettings {
    verbose: matches.is_present("verbose"),
    dry_run: matches.is_present("dry"),
    force:   matches.is_present("force"),
  }
}

