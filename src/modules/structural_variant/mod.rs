mod sv_registry;

////////////////////////////////////////////////////////////////////////////////////////////////////

pub fn sv_controller(
  directory: &str,
  expected_tlen: i32,
  pair_end_reference_alignment: &str,
  hash_map_collection: alias::RecordSV,
  hash_map_anchor: alias::RegistryME,
) -> alias::AnyResult {
  let sv_aligned_file =
    format!("{}{}", directory, pair_end_reference_alignment);

  sv_registry::sv_mapper(
    &sv_aligned_file,
    expected_tlen,
    hash_map_collection,
    hash_map_anchor,
  )?;

  Ok(())
}

