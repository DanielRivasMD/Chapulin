mod sv_registry;

////////////////////////////////////////////////////////////////////////////////////////////////////

pub fn sv_controller(
  directory: &str,
  expected_tlen: i32,
  pair_end_reference_alignment: &str,
  sv_record: alias::RecordSV,
  chr_registry: alias::RegistryChr,
) -> alias::AnyResult {
  let sv_aligned_file =
    format!("{}{}", directory, pair_end_reference_alignment);

  sv_registry::sv_mapper(
    &sv_aligned_file,
    expected_tlen,
    sv_record,
    chr_registry,
  )?;

  Ok(())
}

